package entity;

public class Status {
	private int id;
	private String status_name;

	public Status() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return status_name;
	}

	public void setName(String name) {
		this.status_name = name;
	}
}
